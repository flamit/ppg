function shape = get_shape(canvas,a11,a21,b11,b21,d11,d21,...
    atob1,atod1,atob3,atod3,colour)
%Returns a shape for ROI

shape = insertShape(canvas,'FilledPolygon',...
        [a11+atob1*(b11-a11)+atod1*(d11-a11),a21+atob1*(b21-a21)+atod1*(d21-a21),...
        a11+atob3*(b11-a11)+atod1*(d11-a11),a21+atob3*(b21-a21)+atod1*(d21-a21),...
        a11+atob3*(b11-a11)+atod3*(d11-a11),a21+atob3*(b21-a21)+atod3*(d21-a21),...
        a11+atob1*(b11-a11)+atod3*(d11-a11),a21+atob1*(b21-a21)+atod3*(d21-a21)],...
        'Color', colour, 'Opacity', 1);
end